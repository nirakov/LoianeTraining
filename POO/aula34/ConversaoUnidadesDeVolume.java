package aula34;

public class ConversaoUnidadesDeVolume {
    public static double litrosCm3(double litros){
        return litros * 1000;
    }
    public static double m3Litro(double m3){
        return m3 * 1000;
    }
    public static double m3Pes(double m3){
        return m3 * 35.32;
    }    
    public static double galaoPol3(double galao){
        return galao * 321;
    }
    public static double galaoLitros(double galao){
        return galao * 3.785;
    }
}
