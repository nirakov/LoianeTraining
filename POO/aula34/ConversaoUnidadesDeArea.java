package aula34;

public class ConversaoUnidadesDeArea {
    public static double metroPes(double metros){
        return metros * 10.76;
    }

    public static double pesCentimetros(double pes){
        return pes * 928;
    }

    public static double milhasAcres(double milhas){
        return milhas * 640;
    }

    public static double acrePes(double acres){
        return acres * 43.56;
    }
    
}
